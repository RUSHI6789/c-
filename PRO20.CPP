#include<iostream>
using namespace std;
int main()
{
    cout<<"\n 10>10 :"<<(10>10);
    cout<<"\n 10<10 :"<<(10<10);
    cout<<"\n 10>=10 :"<<(10>=10);
    cout<<"\n 10<=10 :"<<(10<=10);
    cout<<"\n 10==10 :"<<(10==10);
    cout<<"\n 10==11 :"<<(10==11);
    cout<<"\n 10!=10 :"<<(10!=10);
    cout<<"\n 10!=11 :"<<(10!=11);
    return 0;
}